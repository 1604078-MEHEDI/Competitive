const int N = 1e6+1;
const ll inf = 1e17;
vector<vector<ll> > Tree(4 * N, {0, inf});

ll f(vector<ll> line, ll x) {
	return line[0] * x + line[1];
}

void Insert(vector<ll> line, ll l = 1,ll r = N, ll i = 1) {
	ll m = (l + r) / 2;
	bool left = f(line, l) < f(Tree[i], l);
	bool mid = f(line, m) < f(Tree[i], m);
	if (mid)swap(Tree[i], line);
	if (r - l == 1)return;
	else if (left != mid)Insert(line, l, m, 2 * i);
	else Insert(line, m, r, 2 * i + 1);
}

ll Query(ll x, ll l = 1, ll r = N, ll i = 1) {
	ll m = (l + r) / 2;
	ll cur = f(Tree[i], x);
	if (r - l == 1)return cur;
	if (x < m)return min(cur, Query(x, l, m, 2 * i));
	else return min(cur, Query(x, m, r, 2 * i + 1));
}



