vector<ll> prefix_function(const string &s) {
	ll n = s.size();
	vector<ll> P(n);
	for (ll i = 1; i < n; i++) {
		ll j = P[i - 1];
		while (j && s[i] != s[j])
			j = P[j - 1];
		if (s[i] == s[j])j++;
		P[i] = j;
	}
	return P;
}

vector<ll> KMP(const string &s, const string &t) {
	//ll n = s.size();
	ll m = t.size();
	string r = t + "#" + s;
	vector<ll> P = prefix_function(r);
	ll l = r.length();
	vector<ll> cnt;
	for (ll i = m + 1; i < l; i++)
		if (P[i] == m)cnt.push_back(i - 2 * m);
	return cnt;
}


